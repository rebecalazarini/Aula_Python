# Crie uma lista com as notas de 10 alunos e calcule a média da turma, a maior nota e a menor nota.
notas = [10, 8, 3, 7, 7, 6, 2, 9, 10, 10]
media = sum(notas) / len(notas)
maior_nota = max(notas)
menor_nota = min(notas)
print("Média:", media)
print("Maior:", maior_nota)   
print("Menor:", menor_nota)